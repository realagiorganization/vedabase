import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const cacheRoot = path.join(repoRoot, "data", "cache");
const generatedPath = path.join(repoRoot, "src", "generated", "remote-data.ts");
const vedabaseCache = JSON.parse(
  fs.readFileSync(path.join(cacheRoot, "vedabase-index.json"), "utf8"),
);
const youtubeCache = JSON.parse(
  fs.readFileSync(path.join(cacheRoot, "youtube-search-index.json"), "utf8"),
);
const vedabaseMetadata = JSON.parse(
  fs.readFileSync(
    path.join(repoRoot, "data", "remote", "vedabase-dump", "metadata.json"),
    "utf8",
  ),
);
const youtubeMetadata = JSON.parse(
  fs.readFileSync(
    path.join(repoRoot, "data", "remote", "youtube-search", "metadata.json"),
    "utf8",
  ),
);
const syncReport = JSON.parse(
  fs.readFileSync(path.join(cacheRoot, "sync-report.json"), "utf8"),
);

const payload = {
  generatedAt: new Date().toISOString(),
  vedabase: vedabaseCache,
  youtube: youtubeCache,
  vedabaseMetadata,
  youtubeMetadata,
  syncReport,
};

const moduleSource = `/* This file is auto-generated by scripts/generate-remote-data-module.mjs. Do not edit manually. */
export const remoteDataIndex = ${JSON.stringify(payload, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(generatedPath), { recursive: true });
fs.writeFileSync(generatedPath, moduleSource);

console.log(`Generated remote data module: ${path.relative(repoRoot, generatedPath)}`);
